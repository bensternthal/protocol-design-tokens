/* Protocol Colors iOS Variables v1.0.0
   From https://github.com/mozilla/protocol-design-tokens/tree/master/protocol-colors#readme */

extension UIColor {
    struct Protocol {
        static let Black = UIColor(rgb: 0x000000)
        static let Primary = UIColor(rgb: 0x000000)

        static let White = UIColor(rgb: 0xffffff)
        static let Secondary = UIColor(rgb: 0xffffff)

        static let Cyanlight = UIColor(rgb: 0x00ffff)
        static let Cyan = UIColor(rgb: 0x00e5e5)
        static let Cyandark = UIColor(rgb: 0x00cccc)

        static let Yellowlight = UIColor(rgb: 0xfff63a)
        static let Yellow = UIColor(rgb: 0xe5dc34)
        static let Yellowdark = UIColor(rgb: 0xccc42e)

        static let Redlight = UIColor(rgb: 0xff4d4e)
        static let Red = UIColor(rgb: 0xe54546)
        static let Reddark = UIColor(rgb: 0xcc3d3e)

        static let Greenlight = UIColor(rgb: 0x49ffbb)
        static let Green = UIColor(rgb: 0x41e5a7)
        static let Greendark = UIColor(rgb: 0x3acc95)

        static let Teallight = UIColor(rgb: 0x00a7a7)
        static let Teal = UIColor(rgb: 0x009696)
        static let Tealdark = UIColor(rgb: 0x008484)

        static let Purplelight = UIColor(rgb: 0xcf2cfa)
        static let Purple = UIColor(rgb: 0xba27e1)
        static let Purpledark = UIColor(rgb: 0xa423c7)

        static let Bluelight = UIColor(rgb: 0x00a7e0)
        static let Blue = UIColor(rgb: 0x0096ca)
        static let Bluedark = UIColor(rgb: 0x0084b2)

  }
}